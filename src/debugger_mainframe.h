#ifdef EASYRPG_DEBUGGER
#ifndef _EASYRPG_DEBUGGER_MAINFRAME_H_
#define _EASYRPG_DEBUGGER_MAINFRAME_H_

/////////////////////////////////////////////////////////////////////////////
// Headers
/////////////////////////////////////////////////////////////////////////////
#include <wx/event.h>
#include "debugger_mainframe_gui.h"
#include "debugger_mainpanel.h"

/////////////////////////////////////////////////////////////////////////////
/// Debugger_MainPanel class.
/// This is the window used by the debugger.
/////////////////////////////////////////////////////////////////////////////
class Debugger_MainFrame : public Debugger_MainFrameGui {

public:
	Debugger_MainFrame();

	static const wxEventType PlayerEvent;

private:
	/////////////////////////////////////////////////////////////////////////
	/// Run-Button in the toolbar was clicked. The player will resume normal
	/// execution.
	/////////////////////////////////////////////////////////////////////////
	void OnRunToolClicked(wxCommandEvent& event);

	/////////////////////////////////////////////////////////////////////////
	/// Pause-Button in the toolbar was clicked. The player will suspend and
	/// send a Player_suspended-event.
	/////////////////////////////////////////////////////////////////////////
	void OnPauseToolClicked(wxCommandEvent& event);

	/////////////////////////////////////////////////////////////////////////
	/// Window was closed.
	/////////////////////////////////////////////////////////////////////////
	void OnFrameClose(wxCloseEvent& event);

	/////////////////////////////////////////////////////////////////////////
	/// The player has suspended. Accessing data structures from the player
	/// is save now.
	/////////////////////////////////////////////////////////////////////////
	void OnPlayerSuspendedEvent(wxCommandEvent& event);

	/////////////////////////////////////////////////////////////////////////
	/// The player was closed, the debugger will shutdown.
	/////////////////////////////////////////////////////////////////////////
	void OnPlayerCloseEvent(wxCommandEvent& event);

	/////////////////////////////////////////////////////////////////////////
	/// Other player events unhandled by the MainFrame. They are forwarded
	/// to the MainPanel.
	/////////////////////////////////////////////////////////////////////////
	void OnOtherPlayerEvent(wxCommandEvent& event);

	DECLARE_EVENT_TABLE();
};

#endif
#endif
